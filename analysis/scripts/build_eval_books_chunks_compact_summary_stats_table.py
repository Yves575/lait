"""Build the compact eval books/chunks summary statistics LaTeX table.

Reads:
  - ``analysis/manuscript_tables/csv/eval_books_chunks_compact_summary_stats.csv``

Output is written to
``analysis/manuscript_tables/tex/eval_books_chunks_compact_summary_stats.tex``
and also printed to stdout.
"""

from __future__ import annotations

import csv
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
INPUT_PATH = (
    REPO_ROOT
    / "analysis"
    / "manuscript_tables"
    / "tables"
    / "csv"
    / "eval_books_chunks_compact_summary_stats.csv"
)
OUTPUT_PATH = (
    REPO_ROOT
    / "analysis"
    / "manuscript_tables"
    / "tables"
    / "tex"
    / "eval_books_chunks_compact_summary_stats.tex"
)

STATS_ORDER = ("mean", "std", "max", "min")
STAT_LABELS = {
    "mean": r"\textsc{Mean}",
    "std": r"\textsc{St. Dev.}",
    "max": r"\textsc{Max}",
    "min": r"\textsc{Min}",
}
SECTION_CONFIG = {
    "books": {
        "label": r"\textbf{Books}",
        "icon": r"\includegraphics[height=1.1em]{figs/books.png}",
    },
    "chunks": {
        "label": r"\textbf{Chunks}",
        "icon": r"\chunkicon",
    },
}


def fmt_intish(value: float) -> str:
    return f"{int(round(value)):,}"


def read_table_rows(
    path: Path,
) -> tuple[dict[str, dict[str, dict[str, float]]], dict[str, int]]:
    """Load CSV into nested section -> stat -> column dict."""
    data: dict[str, dict[str, dict[str, float]]] = {}
    n_by_section: dict[str, int] = {}

    with path.open("r", encoding="utf-8") as fh:
        reader = csv.DictReader(fh)
        for row in reader:
            section = row["section"]
            stat = row["stat"]
            n_by_section[section] = int(row["n"])
            data.setdefault(section, {})[stat] = {
                "htr_tokens": float(row["htr_tokens"]),
                "htr_words": float(row["htr_words"]),
                "mtr_tokens": float(row["mtr_tokens"]),
                "mtr_words": float(row["mtr_words"]),
                "src_tokens": float(row["src_tokens"]),
                "src_words": float(row["src_words"]),
            }

    return data, n_by_section


def build_section_body(
    section: str,
    section_data: dict[str, dict[str, float]],
) -> list[str]:
    lines: list[str] = []
    for stat in STATS_ORDER:
        values = section_data[stat]
        cells = [
            STAT_LABELS[stat],
            fmt_intish(values["htr_tokens"]),
            fmt_intish(values["htr_words"]),
            fmt_intish(values["mtr_tokens"]),
            fmt_intish(values["mtr_words"]),
            fmt_intish(values["src_tokens"]),
            fmt_intish(values["src_words"]),
        ]
        lines.append("        " + " & ".join(cells) + r" \\")
    return lines


def build_table(data: dict[str, dict[str, dict[str, float]]], n_by_section: dict[str, int]) -> str:
    books_cfg = SECTION_CONFIG["books"]
    chunks_cfg = SECTION_CONFIG["chunks"]
    n_books = n_by_section["books"]
    n_chunks = n_by_section["chunks"]
    n_cols = 7

    books_body = build_section_body("books", data["books"])
    chunks_body = build_section_body("chunks", data["chunks"])

    header = (
        "% Auto-generated by build_eval_books_chunks_compact_summary_stats_table.py; "
        "do not edit by hand.\n"
        "% Regenerate: python analysis/scripts/export_eval_books_chunks_compact_summary_stats.py\n"
        "%            python analysis/scripts/build_eval_books_chunks_compact_summary_stats_table.py\n"
        "%\n"
    )
    lines = [
        r"\begin{table}[t!]",
        r"    \centering",
        r"    \resizebox{\linewidth}{!}{%",
        r"    \begin{tabular}{lcccccc}",
        r"        \toprule",
        r"        & \multicolumn{2}{c}{\htr}",
        r"        & \multicolumn{2}{c}{\mtr}",
        r"        & \multicolumn{2}{c}{\srcr} \\",
        r"        \cmidrule(lr){2-3} \cmidrule(lr){4-5} \cmidrule(lr){6-7}",
        r"        & \textsc{Tokens} & \textsc{Words}",
        r"        & \textsc{Tokens} & \textsc{Words}",
        r"        & \textsc{Tokens} & \textsc{Words} \\",
        r"        \midrule",
        rf"        \multicolumn{{{n_cols}}}{{l}}{{{books_cfg['label']} {books_cfg['icon']} \hfill (\textit{{n={n_books}}})}} \\",
        r"        \midrule",
        *books_body,
        r"        \midrule",
        rf"        \multicolumn{{{n_cols}}}{{l}}{{{chunks_cfg['label']} {chunks_cfg['icon']} \hfill (\textit{{n={n_chunks}}})}} \\",
        r"        \midrule",
        *chunks_body,
        r"        \bottomrule",
        r"    \end{tabular}",
        r"    }",
        r"    \caption{Summary statistics for the evaluation dataset. "
        r"Words are whitespace-delimited counts. Tokens are computed with "
        r"\texttt{tiktoken} (\texttt{o200k\_base}).}",
        r"    \label{tab:eval_books_chunks_compact_summary_stats}",
        r"\end{table}",
    ]
    return header + "\n".join(lines)


def main() -> None:
    if not INPUT_PATH.is_file():
        raise SystemExit(
            f"Error: {INPUT_PATH.relative_to(REPO_ROOT)} not found. "
            "Run export_eval_books_chunks_compact_summary_stats.py first."
        )

    data, n_by_section = read_table_rows(INPUT_PATH)
    for section in ("books", "chunks"):
        if section not in data:
            raise SystemExit(f"Error: missing section '{section}' in {INPUT_PATH}")

    output = build_table(data, n_by_section) + "\n"
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_PATH.write_text(output, encoding="utf-8")
    print(output)
    print(f"Wrote {OUTPUT_PATH.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    main()
